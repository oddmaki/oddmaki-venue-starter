/**
 * Generate a deterministic pseudonym from an Ethereum address.
 * Uses the last 8 hex characters as a seed to select adjective + noun.
 *
 * @param address - Ethereum address (0x-prefixed or plain hex)
 * @returns Pseudonym like "SwiftFox" or "CosmicNebula"
 */
export function generatePseudonym(address: string): string {
  // Use last 8 hex chars as seed (most entropy in the address)
  const cleanAddr = address.replace("0x", "").toLowerCase();
  const seed = parseInt(cleanAddr.slice(-8), 16);
  const adjIndex = seed % ADJECTIVES.length;
  const nounIndex = Math.floor(seed / ADJECTIVES.length) % NOUNS.length;

  return `${ADJECTIVES[adjIndex]}${NOUNS[nounIndex]}`;
}

/**
 * Generate a short display address (0x1234...abcd)
 */
export function shortenAddress(address: string, chars = 4): string {
  const addr = address.startsWith("0x") ? address : `0x${address}`;

  return `${addr.slice(0, chars + 2)}...${addr.slice(-chars)}`;
}
